    private Boolean checkTextField() {
        if (txtName.getText().trim().equalsIgnoreCase("")) {
            ImageIcon icon = new ImageIcon(getClass().getResource("/images/warning.png"));
            JOptionPane.showConfirmDialog(this, "Name Activity can't be blank !", "Error", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE, icon);
            return false;
        } else if (txtRates.getText().trim().equalsIgnoreCase("")) {
            ImageIcon icon = new ImageIcon(getClass().getResource("/images/warning.png"));
            JOptionPane.showConfirmDialog(this, "Rates can't be blank !", "Error", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE, icon);
            return false;
        }

        double d;
        try {
            d = Double.parseDouble(txtRates.getText().trim());
            if (d <= 0) {
                ImageIcon icon = new ImageIcon(getClass().getResource("/images/warning.png"));
                JOptionPane.showConfirmDialog(this, "Invalid number", "Error", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE, icon);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid number");
            return false;
        }


        if (!txtName.getText().equalsIgnoreCase(nameActive)) {
            try {
                if (new Business("tbl_Activities", sConn).checkActivity("tbl_Activities", txtName.getText().trim()).next()) {
                    JOptionPane.showMessageDialog(null, "Name Activity exist !");
                    return false;
                }
            } catch (SQLException ex) {
            }
        }
        return true;
    }

    private void updateEvent(String tableName) {
        ImageIcon icon = new ImageIcon(getClass().getResource("/images/quest.png"));
        int choice = JOptionPane.showConfirmDialog(this, "Are you sure?", "Edit Activity", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon);
        if (choice == JOptionPane.YES_OPTION) {
            try {
                new Business(tableName, sConn).updateActivity(tableName, txtName.getText().trim(), txtRates.getText().trim(), activeCode);
            } catch (SQLException ex) {
                Logger.getLogger(addActivity.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }

